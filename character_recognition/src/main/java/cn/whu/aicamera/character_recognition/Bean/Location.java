package cn.whu.aicamera.character_recognition.Bean;

public class Location {

    private int height;
    private int left;
    private int top;
    private int width;
    public void setHeight(int height) {
        this.height = height;
    }
    public int getHeight() {
        return height;
    }

    public void setLeft(int left) {
        this.left = left;
    }
    public int getLeft() {
        return left;
    }

    public void setTop(int top) {
        this.top = top;
    }
    public int getTop() {
        return top;
    }

    public void setWidth(int width) {
        this.width = width;
    }
    public int getWidth() {
        return width;
    }
}
